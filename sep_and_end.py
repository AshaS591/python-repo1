# sep is the separator which separates variables by space by default in the print function ,if we want separate variables or values specifically we can do that using the sep 
print(10,20) #by default separated by space
name='asha'
print(name,'hello',sep='@')
print(name,'means having desire',sep=' : ')
print('asha','anu','haripriya','earth','sky','rain',sep=' hey ')
print('🤨🙂😑🤔🤩☺️😚😥😮😶🙄😏',sep='%%')
print("🤩",'😍','😁','😂','🤣','🥰','😅','🤗','🙂',sep='100%')
print('python','sql','html','css','javascript',sep='\n') #\n means new line

