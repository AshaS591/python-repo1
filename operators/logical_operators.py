# and ,or ,not --->returns bool 

# and 
print(-10<-2 and 90>89.99)
str1='asha'
srt2='ASHA'
print(str1<srt2 and str1<'zoo')

#########################################################
#########################################################

# or
print(-10<-2 or 90>89.99)
str1='asha'
srt2='ASHA'
print(str1<srt2 or str1<'zoo')

#########################################################
#########################################################

# not
print(not(-10<-2 and 90>89.99))
str1='asha'
srt2='ASHA'
print(not(str1<srt2 and str1<'zoo'))
print(not(-10<-2 or 90>89.99))
str1='asha'
srt2='ASHA'
print(not(str1<srt2 or str1<'zoo'))